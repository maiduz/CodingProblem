package src.codingchallenge;
//Write a program in Java to print a message using escape sequences such as newline, tab, and backslash.
public class EscapeSequences {
    public static void main(String[] args) {
            System.out.println("Thank\tYou!"); // prints Hello    world! using tab escape sequence
            System.out.println("Print\nthe\ncomment!"); // prints Welcome on one line and to and Java on separate lines using newline escape sequence
            System.out.println("This is a backslash: \\"); // prints This is a backslash: \
        }

    }

